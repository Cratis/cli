using System.Text;

namespace Cratis.Cli.Commands.Completions;

/// <summary>
/// Generates a bash completion script for the Cratis CLI.
/// </summary>
public static class BashCompletionGenerator
{
    /// <summary>
    /// Generates the complete bash completion script.
    /// </summary>
    /// <returns>The bash completion script content.</returns>
    public static string Generate()
    {
        var sb = new StringBuilder();
        sb.AppendLine("# bash completion for cratis")
            .AppendLine("# Generated by: cratis completions bash")
            .AppendLine()
            .AppendLine("_cratis() {")
            .AppendLine("    local cur prev words cword")
            .AppendLine("    _init_completion || return")
            .AppendLine()
            .AppendLine($"    local global_opts=\"{string.Join(' ', CliCommandTree.GlobalOptions)}\"")
            .AppendLine();

        var commands = CliCommandTree.Commands;
        var topLevelNames = string.Join(' ', commands.Select(c => c.Name));

        sb.AppendLine("    case \"${COMP_WORDS[1]}\" in");

        foreach (var cmd in commands)
        {
            if (cmd.Children.Count > 0)
            {
                GenerateBranch(sb, cmd, 1);
            }
            else
            {
                var opts = string.Join(' ', cmd.Options);
                sb.AppendLine($"        {cmd.Name})")
                    .AppendLine($"            COMPREPLY=( $(compgen -W \"{opts} $global_opts\" -- \"$cur\") )")
                    .AppendLine("            return ;;");
            }
        }

        sb.AppendLine("    esac")
            .AppendLine()
            .AppendLine($"    COMPREPLY=( $(compgen -W \"{topLevelNames} $global_opts\" -- \"$cur\") )")
            .AppendLine("}")
            .AppendLine()
            .AppendLine("complete -F _cratis cratis");

        return sb.ToString();
    }

    static void GenerateBranch(StringBuilder sb, CommandNode node, int depth)
    {
        var childNames = string.Join(' ', node.Children.Select(c => c.Name));
        sb.AppendLine($"        {node.Name})")
            .AppendLine($"            case \"${{COMP_WORDS[{depth + 1}]}}\" in");

        foreach (var child in node.Children)
        {
            if (child.Children.Count > 0)
            {
                var grandChildNames = string.Join(' ', child.Children.Select(c => c.Name));
                sb.AppendLine($"                {child.Name})")
                    .AppendLine($"                    case \"${{COMP_WORDS[{depth + 2}]}}\" in");

                foreach (var grandChild in child.Children)
                {
                    var opts = string.Join(' ', grandChild.Options);
                    sb.AppendLine($"                        {grandChild.Name})")
                        .AppendLine($"                            COMPREPLY=( $(compgen -W \"{opts} $global_opts\" -- \"$cur\") )")
                        .AppendLine("                            return ;;");
                }

                sb.AppendLine("                    esac")
                    .AppendLine($"                    COMPREPLY=( $(compgen -W \"{grandChildNames} $global_opts\" -- \"$cur\") )")
                    .AppendLine("                    return ;;");
            }
            else
            {
                var opts = string.Join(' ', child.Options);
                sb.AppendLine($"                {child.Name})")
                    .AppendLine($"                    COMPREPLY=( $(compgen -W \"{opts} $global_opts\" -- \"$cur\") )")
                    .AppendLine("                    return ;;");
            }
        }

        sb.AppendLine("            esac")
            .AppendLine($"            COMPREPLY=( $(compgen -W \"{childNames} $global_opts\" -- \"$cur\") )")
            .AppendLine("            return ;;");
    }
}
